package me.mrletsplay.videobase.exception;

public class URLProxyException extends Exception {

	private static final long serialVersionUID = -333151869343838424L;

	public URLProxyException() {
		super();
	}

	public URLProxyException(String message, Throwable cause) {
		super(message, cause);
	}

	public URLProxyException(String message) {
		super(message);
	}

	public URLProxyException(Throwable cause) {
		super(cause);
	}

}
