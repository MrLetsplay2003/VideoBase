package me.mrletsplay.videobase.exception;

public class LibraryUpdateException extends Exception {

	private static final long serialVersionUID = -7103909600710548044L;

	public LibraryUpdateException() {
		super();
	}

	public LibraryUpdateException(String message, Throwable cause) {
		super(message, cause);
	}

	public LibraryUpdateException(String message) {
		super(message);
	}

	public LibraryUpdateException(Throwable cause) {
		super(cause);
	}

}
